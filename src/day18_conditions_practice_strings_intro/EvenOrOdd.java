package day18_conditions_practice_strings_intro;

public class EvenOrOdd {
    public static void main(String[] args) {
        int num = 10;
        if (num % 2 == 0) {
            System.out.println("even"); // 5/2:2+2+ remainder
            System.out.println();
        } else {
            System.out.println("odd");
        }

    }
}